CREATE TABLE IF NOT EXISTS mainmenu (
id integer PRIMARY KEY AUTOINCREMENT,
title TEXT NOT NULL,
url TEXT NOT NULL);

CREATE TABLE IF NOT EXISTS posts(
id integer PRIMARY KEY AUTOINCREMENT,
title TEXT NOT NULL,
intro TEXT NOT NULL,
text TEXT NOT NULL,
url TEXT NOT NULL,
time integer NOT NULL);


CREATE TABLE IF NOT EXISTS users(
id integer PRIMARY KEY AUTOINCREMENT,
name TEXT NOT NULL,
email TEXT NOT NULL,
psw TEXT NOT NULL,
avatar BLOB DEFAULT NULL,
time integer NOT NULL);
